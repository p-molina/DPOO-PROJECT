package Presentation;

/**
 * Enumeración que define las opciones disponibles en el menú de la interfaz de usuario.
 * Esta enumeración facilita la gestión y la lectura del código al trabajar con las diversas pantallas y opciones del menú.
 */
public enum MenuOptions {
    /**
     * Opción para mostrar el menú principal.
     */
    MENU_PRINCIPAL,
    /**
     * Opción para la verificación de archivos en el sistema.
     */
    CHECKING_FILES,
    /**
     * Opción para salir del programa.
     */
    EXIT_PROGRAM,
    /**
     * Opción para iniciar el programa.
     */
    START_PROGRAM,
    /**
     * Opción para regresar al menú principal.
     */
    MAIN_MENU,
    /**
     * Opción para seleccionar una categoría de productos o servicios.
     */
    SELECT_CATEGORY,
    /**
     * Opción para mostrar el menú de productos.
     */
    MENU_PRODUCTO,
    /**
     * Opción para gestionar las tiendas.
     */
    MANAGE_SHOPS,
    /**
     * Opción para seleccionar un modelo de producto.
     */
    SELECT_MODEL,
    /**
     * Opción para acceder al menú de eliminación.
     */
    DELETE_MENU,
    /**
     * Opción para listar las tiendas disponibles.
     */
    LIST_SHOPS,
    /**
     * Opción para gestionar el catálogo de productos.
     */
    MANAGE_CATALOGUE_PRODUCT,
    /**
     * Opción para acceder al menú de reseñas.
     */
    MENU_REVIEWS,
    /**
     * Opción para acceder al menú del carro de compra.
     */
    MENU_CARD
}
